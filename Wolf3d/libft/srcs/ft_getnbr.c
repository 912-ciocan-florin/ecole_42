#include "libft.h"

int		ft_getnbr(char *str)
{
	int		nb;
	int		neg;

	nb = 0;
	neg = 1;
	if (str[0] == '-')
	{
		neg = -1;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		nb = (nb * 10) + (*str - '0');
		str++;
	}
	return (nb * neg);
}
